package qn.four.shapeschoser;

public class Rectangle {
	  public  double length;
	   public double width;

	    Rectangle(double length, double width) {
	        this.length = length;
	        this.width = width;
	    }


	   public double calculateArea() {
	        return length * width;
	    }
	}



