package qn.one.rectangle;

import java.util.Scanner;

	public	class Rectangle {
		 
		   public int length;
		  public  int width;

		    Rectangle(int length, int width) {
		        this.length = length;
		        this.width = width;
		    }

			public double calculateArea(){
				return this.length*this.width;}
		    
		}

		